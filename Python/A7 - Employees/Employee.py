class Employee:
    def __init__(self, name, id, dept, job):
        self.__name = name
        self.__id = id
        self.__dep = dept
        self.__job = job

#****************************************************************************************************

    def get_name(self):
        return self.__name

#****************************************************************************************************

    def get_id(self):
        return self.__id

#****************************************************************************************************

    def get_dept(self):
        return self.__dep

#****************************************************************************************************

    def get_job(self):
        return self.__job

#****************************************************************************************************

    def set_name(self, name):
        self.__name = name

#****************************************************************************************************

    def set_id(self, id):
        self.__id = id

#****************************************************************************************************

    def set_dept(self, dep):
        self.__dep = dep

#****************************************************************************************************

    def set_job(self, job):
        self.__job = job

#****************************************************************************************************

    def __str__(self):
        return (f'Name: {self.__name}\n'
                f'ID number: {self.__id}\n'
                f'Department: {self.__dep}\n'
                f'Title: {self.__job}')

