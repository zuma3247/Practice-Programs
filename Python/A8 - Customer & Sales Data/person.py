class Person:
    def __init__(self, name, telephone):
        self.__name = name
        self.__telephone = telephone

#****************************************************************************************************

    def get_name(self):
        return self.__name

#****************************************************************************************************

    def set_name(self, name):
        self.__name = name

#****************************************************************************************************

    def get_telephone(self):
        return self.__telephone

#****************************************************************************************************

    def set_telephone(self, telephone):
        self.__telephone = telephone

